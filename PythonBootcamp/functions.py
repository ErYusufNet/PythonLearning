def happy_birthay(name, age):
    print(f"Happy Birthay {name}")
    print(f"You are {age} years old.")
happy_birthay("Yusuf", 30)

def greet(name, city):
    print(f"Hello, {name}! Welcome from {city}")
    print("We hope you enjoy your stay.")
greet("Yusuf", "Helsinki")